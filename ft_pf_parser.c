#include "ft_printf.h"
#include "ft_pf_utils.h"

int
is_flag_width_prec(const char c)
{
	if (('0' <= c && c <= '9') ||
		 c == '#' || c == '+' || c == '-' || c == ' ' || c == '.' || c == '*')
		return (1);
	else
		return (0);
}

